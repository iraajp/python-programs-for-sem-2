#List Example
Hardware = ["Printer", "Mouse", "Keyboard"]
print(Hardware)

#Change item in a list
Hardware = ["Printer", "Mouse", "Keyboard"]
Hardware[1] = "HDD"
print(Hardware)

#add item
Hardware = ["Printer", "Mouse", "Keyboard"]
Hardware.append("CPU")
print(Hardware)

#remove an item
Hardware = ["Printer", "Mouse", "Keyboard"]
Hardware.remove("Printer")
print(Hardware)